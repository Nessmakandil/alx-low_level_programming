#include "3-calc.h"

/**
 * op_add - add.
 * 
 * @a: number 1.
 * @b: number 1.
 * 
 * Return: summation.
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - sub.
 * 
 * @a: number 1.
 * @b: number 1.
 * 
 * Return: subtraction.
 */
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - mul.
 * 
 * @a: number 1.
 * @b: number 1.
 * 
 * Return: multiplication.
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - div.
 * 
 * @a: number 1.
 * @b: number 1.
 * 
 * Return: division.
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - mod.
 * 
 * @a: number 1.
 * @b: number 1.
 * 
 * Return: modules.
 */
int op_mod(int a, int b)
{
    return (a % b);
}